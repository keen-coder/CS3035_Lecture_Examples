#-----------------------STRING REPRESENTATIONS OF CLASSES-----------------------

# Python has special methods which can be implemented in any class whose job it
# is to return a string representation of that class.

# These methods are the __str__() and __repr__() methods.

# Traditionally __str__() is use to return a string representation for the end
# user while __repr__() is mainly used for debugging and development.

# see the example classes included this week.

