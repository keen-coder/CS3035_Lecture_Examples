# Use the built-in input() function to read data from the console
# The input function can take a prompt (string to say what the input should be)
# and always reads everything as a string.  You must use a conversion function
# to convert the input data to the type you want.

# Reads the input as a string
name = input('Enter your name: ')

# Reads the input as a string and converts to an int.
age = int(input('Enter your age: '))

# Reads the input as a string and converts to a float.
value = float(input('Enter a floating-point value: '))